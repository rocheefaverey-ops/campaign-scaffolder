#!/usr/bin/env node
import { spawnSync } from 'child_process';
import { existsSync, mkdirSync, rmSync, readdirSync, statSync, readFileSync } from 'fs';
import { join, relative, resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const REPO_ROOT = resolve(dirname(__filename), '..');
const ANTIGRAV = resolve(REPO_ROOT, '..', 'antigravfix');
const TMP_DIR = resolve('C:/tmp/_verify_antigravfix_tmp');

const IGNORED_PATHS = new Set([
  '.git',
  'frontend/.git',
  'frontend/.next',
  'frontend/node_modules',
  'frontend/package-lock.json',
  'frontend/tsconfig.tsbuildinfo',
  'frontend/next-env.d.ts',
]);

const TEXT_EXTENSIONS = new Set([
  '.js', '.jsx', '.ts', '.tsx', '.json', '.md', '.css', '.scss', '.mjs', '.cjs',
  '.html', '.txt', '.env', '.dist', '.yml', '.yaml', '.svg', '.xml', '.scaffolded',
]);

function clean(dir) {
  if (existsSync(dir)) rmSync(dir, { recursive: true, force: true });
}

function isIgnored(rel) {
  const posix = rel.replace(/\\/g, '/');
  if (IGNORED_PATHS.has(posix)) return true;
  for (const ignored of IGNORED_PATHS) {
    if (posix.startsWith(`${ignored}/`)) return true;
  }
  return false;
}

function readComparable(path) {
  const text = readFileSync(path, 'utf8');
  return text
    .replace(/"(createdAt|scaffoldedAt|updatedAt)":\s*"[^"]+"/g, '"$1": "<MASKED>"')
    .replace(/Generated \d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2} UTC/g, 'Generated <MASKED>')
    .replace(/\*Generated by Livewall Campaign Scaffolder — .*?\*/g, '*Generated by Livewall Campaign Scaffolder — <MASKED>*')
    .replace(/"recreateCommand":\s*"[\s\S]*?"(?=,\n)/, '"recreateCommand": "<MASKED>"')
    .replace(/\r\n/g, '\n')
    .replace(/\n+$/g, '');
}

function compareFiles(a, b, rel) {
  const ext = rel.slice(rel.lastIndexOf('.'));
  if (TEXT_EXTENSIONS.has(ext) || !ext) {
    return readComparable(a) === readComparable(b);
  }
  return readFileSync(a).equals(readFileSync(b));
}

function walkAndDiff() {
  const drift = [];
  function visit(rel = '') {
    if (rel && isIgnored(rel)) return;
    const expected = rel ? join(ANTIGRAV, rel) : ANTIGRAV;
    const actual = rel ? join(TMP_DIR, rel) : TMP_DIR;
    const stExpected = existsSync(expected) ? statSync(expected) : null;
    const stActual = existsSync(actual) ? statSync(actual) : null;

    const relPosix = rel.replace(/\\/g, '/') || '.';
    if (stExpected && !stActual) { drift.push(`MISSING in scaffold output: ${relPosix}`); return; }
    if (!stExpected && stActual) { drift.push(`UNEXPECTED in scaffold output: ${relPosix}`); return; }
    if (!stExpected || !stActual) return;
    if (stExpected.isDirectory() !== stActual.isDirectory()) {
      drift.push(`TYPE MISMATCH: ${relPosix}`);
      return;
    }
    if (stExpected.isDirectory()) {
      const names = new Set([...readdirSync(expected), ...readdirSync(actual)]);
      for (const name of [...names].sort()) visit(rel ? join(rel, name) : name);
      return;
    }
    if (!compareFiles(expected, actual, rel)) drift.push(`CONTENT DIFFERS: ${relPosix}`);
  }
  visit();
  return drift;
}

clean(TMP_DIR);

const result = spawnSync('node', [
  join(REPO_ROOT, 'cli', 'scaffold.js'),
  '--name=antigravfix',
  '--cape-id=61122',
  '--market=NL',
  '--game=unity',
  '--page=video',
  '--page=landing',
  '--page=onboarding',
  '--page=video-2',
  '--page=game',
  '--page=result',
  `--output=${TMP_DIR}`,
  '--skip-install',
  '--skip-git',
  '--yes',
], { cwd: REPO_ROOT, stdio: 'inherit' });

if (result.status !== 0) {
  console.error('[verify] Scaffold failed; aborting diff.');
  process.exit(2);
}

const drift = walkAndDiff();
if (drift.length === 0) {
  console.log('[verify] OK - scaffolder output matches antigravfix/.');
  clean(TMP_DIR);
  process.exit(0);
}

console.error('[verify] Drift detected:');
for (const line of drift) console.error(`  ${line}`);
console.error(`\n[verify] ${drift.length} drift entr${drift.length === 1 ? 'y' : 'ies'}. Output kept at: ${TMP_DIR}`);
process.exit(1);
